'use strict';

// MODULES //

var isnan = require( './../../../../base/assert/is-nan' );
var floor = require( './../../../../base/special/floor' );
var isNegativeZero = require( './../../../../base/assert/is-negative-zero' );


// MAIN //

/**
* Rounds a numeric value to the nearest integer.
*
* @param {number} x - input value
* @returns {number} function value
*
* @example
* var v = round( -4.2 );
* // returns -4.0
*
* @example
* var v = round( -4.5 );
* // returns -4.0
*
* @example
* var v = round( -4.6 );
* // returns -5.0
*
* @example
* var v = round( 9.99999 );
* // returns 10.0
*
* @example
* var v = round( 9.5 );
* // returns 10.0
*
* @example
* var v = round( 9.2 );
* // returns 9.0
*
* @example
* var v = round( 0.0 );
* // returns 0.0
*
* @example
* var v = round( -0.0 );
* // returns -0.0
*
* @example
* var v = round( Infinity );
* // returns Infinity
*
* @example
* var v = round( -Infinity );
* // returns -Infinity
*
* @example
* var v = round( NaN );
* // returns NaN
*/
function round( x ) {
	if ( isnan( x ) ) {
		return NaN;
	}
	if ( isNegativeZero( x ) || ( x >= -0.5 && x < 0.0 ) ) {
		return -0.0;
	}
	if ( x > 0.0 && x < 0.5 ) {
		return 0.0;
	}
	return floor( x + 0.5 );
}


// EXPORTS //

module.exports = round;
